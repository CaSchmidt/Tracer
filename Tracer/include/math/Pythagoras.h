#ifndef PYTHAGORAS_H
#define PYTHAGORAS_H

#include <algorithm>

namespace math {

  template<typename T>
  constexpr T pythagoras(const T& x)
  {
    // x^2 + y^2 = 1  =>  y = sqrt(1 - x^2)
    return std::sqrt(std::max<T>(0, static_cast<T>(1) - x*x));
  }

} // namespace math

#endif // PYTHAGORAS_H
